#include<iostream>
using namespace std;
int main()
{
          int t;
          cin>>t;
          while(t>0)
          {
                    int a,b,c;
                    cin>>a>>b;
                    c=a+b;
                    cout<<c<<"\n";
                    t--;
          }
          return 0;
}
